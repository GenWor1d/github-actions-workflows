#!/bin/bash

# 预部署脚本示例
# 执行自定义的预部署任务

set -e  # 遇到错误立即退出

echo "========================================"
echo "开始执行自定义预部署脚本"
echo "当前时间: $(date)"
echo "当前目录: $(pwd)"
echo "========================================"

# 加载环境变量
if [ -f ".env" ]; then
    echo "[DEBUG] 加载环境变量..."
    source ".env"
    echo "[DEBUG] 环境变量加载成功"
else
    echo "[DEBUG] 未找到 .env 文件，跳过环境变量加载"
fi

# 可以在这里添加自定义的预部署任务
# 例如：
# - 数据库备份
# - 配置文件检查
# - 依赖检查
# - 其他自定义操作

echo "预部署检查完成！"
echo "========================================"